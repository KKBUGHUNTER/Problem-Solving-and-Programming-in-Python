#  check if a given letter is a vowel or consonant using list
while 1==1:
     inp=input("enter the character to check vowel or not = ")

     letter=['A','E','I','O','U',"a","e","i","o","u"]

     if inp in letter:
          print("The given letter is a vowel :)") 

     else:
          print("The given letter is a consonant :)")
# the ( in ) key word is to check the given value is in there are not.
